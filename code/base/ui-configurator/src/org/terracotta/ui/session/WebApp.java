package org.terracotta.ui.session;

public class WebApp {
  private String name;
  private String path;

  public WebApp(String name, String path) {
    this.name = name;
    this.path = path;
  }

  public String getName() {
    return name;
  }

  public String getPath() {
    return path;
  }

  public String toString() {
    return name;
  }
}
